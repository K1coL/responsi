    private void tablePegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePegawaiMouseClicked
        // TODO add your handling code here:
        int rowIdx = tablePegawai.getSelectedRow();

        String nama = tablePegawai.getValueAt(rowIdx, 0).toString();
        txtNama.setText(nama);

        String no = tablePegawai.getValueAt(rowIdx, 1).toString();
        txtNoPegawai.setText(no);

        String posisi = tablePegawai.getValueAt(rowIdx, 2).toString();
        switch (posisi) {
            case "Senior Programmer":
                cmbPosisi.setSelectedIndex(0);
                txtGaji.setText("15.000.000");
                break;
            case "Bussiness Analyst":
                cmbPosisi.setSelectedIndex(1);
                txtGaji.setText("21.000.000");
                break;
            case "Data Office Center":
                cmbPosisi.setSelectedIndex(2);
                txtGaji.setText("18.000.000");
                break;
            case "Junior Programmer":
                cmbPosisi.setSelectedIndex(3);
                txtGaji.setText("8.000.000");
                break;
        }
        
        String id = tablePegawai.getValueAt(rowIdx, 4).toString();
        txtID.setText(id);
        
    }//GEN-LAST:event_tablePegawaiMouseClicked

