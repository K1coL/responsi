    private void loadToTable(){
        model.setRowCount(0);
        
        try{
            db = KoneksiDB.getKoneksi();
            Statement st = db.createStatement();
            
            String sql = "SELECT * FROM pegawai";
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                Object[] data = new Object[7];
                data[0] = rs.getString("nama");
                data[1] = rs.getString("noPegawai");
                data[2] = rs.getString("posisi");
                data[3] = rs.getString("gaji");
                data[4] = rs.getInt("id");
                
                model.addRow(data);
            }
            rs.close();
            st.close();
        }catch(SQLException e){
            System.err.println("load data error : "+ e.getMessage());
        }finally{
            txtID.setEnabled(false);
            txtGaji.setText("15.000.000");
            txtGaji.setEnabled(false);
            tablePegawai.getColumnModel().getColumn(4).setMinWidth(0);
            tablePegawai.getColumnModel().getColumn(4).setMaxWidth(0);
        }
    }
    
     public void printall()throws NamaDanNoPegawaiHarusDiisi,NoPegawaiHarusAngka,NoPegawaiHarus10Digit{
        if(txtNama.getText().isEmpty()|| txtNoPegawai.getText().isEmpty()){
            throw new NamaDanNoPegawaiHarusDiisi();
        }try{
            Long.parseLong(txtNoPegawai.getText());
        }catch(NumberFormatException e){
            throw new NoPegawaiHarusAngka();
        }if (txtNoPegawai.getText().length() != 10){
            throw new NoPegawaiHarus10Digit();
        }      
    }
