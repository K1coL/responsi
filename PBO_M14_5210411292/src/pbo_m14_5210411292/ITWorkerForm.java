    private void bttnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnUpdateActionPerformed
        // TODO add your handling code here:
        try{
            this.printall();
        }catch(NoPegawaiHarusAngka e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());    
        } catch (NamaDanNoPegawaiHarusDiisi e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }catch (NoPegawaiHarus10Digit e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        
        try {
            db = KoneksiDB.getKoneksi();

            String sql = "UPDATE pegawai "
                    + "SET nama=?, noPegawai=?, posisi=?, gaji=? WHERE id=?;";

            PreparedStatement ps = db.prepareStatement(sql);

            ps.setString(1, txtNama.getText());
            ps.setString(2, txtNoPegawai.getText());
            ps.setString(3, cmbPosisi.getSelectedItem().toString());
            ps.setString(4, txtGaji.getText());
            ps.setString(5, txtID.getText());
            
            int rowInsert = ps.executeUpdate();
            if (rowInsert > 0) {
                JOptionPane.showMessageDialog(this, "Data Successfully Updated", "Success", JOptionPane.INFORMATION_MESSAGE);
            }

            ps.close();
               
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Data Failed to Update", "Error", JOptionPane.ERROR_MESSAGE);
        }
        finally {
            this.loadToTable();
        }
       
    }//GEN-LAST:event_bttnUpdateActionPerformed

    private void bttnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            db = KoneksiDB.getKoneksi();

            String sql = "DELETE FROM pegawai "
                    + "WHERE id=?;";

            PreparedStatement ps = db.prepareStatement(sql);

            ps.setString(1, txtID.getText());

            int rowInsert = ps.executeUpdate();
            if (rowInsert > 0) {
                JOptionPane.showMessageDialog(this, "Data Successfully Deleted", "Success", JOptionPane.INFORMATION_MESSAGE);
            }

            ps.close();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Failed to Delete", "Error", JOptionPane.ERROR_MESSAGE);
        }
        finally {
            this.loadToTable();
            txtNama.setText("");
            txtNoPegawai.setText("");
            cmbPosisi.setSelectedIndex(0);
            txtGaji.setText("15.000.000");
            txtGaji.setEnabled(false);
        }
    }//GEN-LAST:event_bttnDeleteActionPerformed

